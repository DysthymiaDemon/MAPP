package mapp.com.sg.projectattendancetracker;

public class PastAttn {
    private String month;
    private String attnRate;
    private String leaves;

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public String getAttnRate() {
        return attnRate;
    }

    public void setAttnRate(String attnRate) {
        this.attnRate = attnRate;
    }

    public String getLeaves() {
        return leaves;
    }

    public void setLeaves(String leaves) {
        this.leaves = leaves;
    }
}
